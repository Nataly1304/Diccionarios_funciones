# funciones con parametros 
#Ejercicio: Escriba una función en Python que reproduzca lo siguiente: 𝑓(𝑥, 𝑦) = 𝑥2 + 𝑦2  Valor para X=3 y valor para Y=5
def f(x, y):
    return x*2 + y*2  # Definición de una función llamada 'f' que toma dos argumentos, 'x' y 'y', y retorna el resultado de la expresión x*2 + y*2.

x = 3  # le asignamos a la variable 'x' el valor de 3.
y = 5  # le asignamos a la variable 'y' el valor de 5.
result = f(x, y)  # Llamada a la función 'f' con los valores de 'x' y 'y' como argumentos, donde se le asigna el resultado a la variable 'result'.
print(result)  
